"""REAP (Regional Education Applicant Program) per-state K-12 portals — free, no
key, public applicant sites. The single best ToS-safe channel for K-12 district
jobs (districts otherwise live on Frontline/AppliTrack — no clean feed — or
NEOGOV, which is ToS-blocked and must NEVER be scraped).

REAP is a family of per-STATE public teacher-job portals under the *reap.net
domain family, each a light PHP site generated by "Cybermill Communication".
Public job search is FREE to seekers with no login wall. Verified live
2026-07-02:

    search page:   https://www.{portal}/jobsrch.php?srch={cat}&position={pos}
    result rows:   <tr class="jobfirstrow"> with a /job_postings/{id}/{site}/{site}
                   detail link, a title (+ <div> subtitle + "Certificate:" line),
                   a school cell "District<br/>City,&nbsp;ST&nbsp;ZIP", and a date.

ROBOTS/ToS: every portal returns 404 for /robots.txt (no crawl restrictions
declared → allowed by default; checked live for usreap/moreap/ohreap/pareap on
2026-07-02). The site states it is "a FREE service to all applicants" and the
job search is a public, no-login page. We read it politely (a low rate limit, a
handful of category pages per covered state, cached once per cycle) and honor
the per-portal robots.txt at build time — if a portal's robots.txt ever appears
and disallows the search path, that portal is dropped (see _portal_allows).

STATE SCOPING: REAP only publishes portals for a handful of states (captured live
from usreap.net/otherStates.php on 2026-07-02: CT, MO, NM, OH, PA). A user in a
NON-covered state gets 0 results with NO noise (self-skip) — REAP simply doesn't
serve their state. Ohio (the teacher-columbus persona's state) IS covered.

INDUSTRY GATING: self-skips unless the active project's industry is an
education-family field (shared _EDUCATION_TOKENS, same gate as HigherEdJobs) AND
the user's location resolves to a covered state. An eng/nursing/trade project, or
an education seeker outside the 5 covered states, polls no REAP portal — so this
is safe to add to DAILY_SOURCES (inert for everyone it doesn't serve).
"""
from __future__ import annotations

import functools
